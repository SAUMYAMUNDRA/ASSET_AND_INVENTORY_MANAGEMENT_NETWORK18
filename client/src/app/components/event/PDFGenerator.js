      const equipmentHeaders = hasHiredEquipment
        ? ['#', 'Equipment', 'Model', 'Qty', 'Install', 'Done', 'In-House', 'Hired', 'Vendor', 'Remarks', 'Status']
        : ['#', 'Equipment', 'Model', 'Qty', 'Install', 'Done', 'In-House', 'Remarks', 'Status'];

