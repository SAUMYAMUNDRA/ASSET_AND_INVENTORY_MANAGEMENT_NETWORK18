    doc.setFontSize(8);

    // Print day details in two columns
    dayDetails.forEach((item, index) => {
      const col = index % 2 === 0 ? margin : margin + 100;
      const row = Math.floor(index / 2);

      doc.setFont('helvetica', 'bold');
      doc.setTextColor(0, 51, 102);
      doc.text(item.label, col, currentY + row * 4.5);

      doc.setFont('helvetica', 'normal');
      doc.setTextColor(0, 0, 0);
      const labelWidth = doc.getTextWidth(item.label) + 3;
      doc.text(item.value || '-', col + labelWidth, currentY + row * 4.5);
    });
